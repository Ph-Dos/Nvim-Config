local opt = vim.opt

-- Set a tab size of 4 spaces
opt.tabstop = 4 -- A TAB character looks like 4 spaces
opt.shiftwidth = 4 -- An indentation level is 4 spaces
opt.softtabstop = 4 -- Pressing TAB or Backspace inserts/deletes 4 spaces

-- Use spaces instead of tabs
opt.expandtab = true

